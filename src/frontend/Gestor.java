    public String ExpresionRegular(String expresion) {
        String aux = "";
        for (int i = 0; i < expresion.length(); i++) {
            if (Character.isLetter(expresion.charAt(i))) {
                aux += "[" + Character.toLowerCase(expresion.charAt(i)) + Character.toUpperCase(expresion.charAt(i)) + "]";
            } else {
                aux += "[" + expresion.charAt(i) + "]";
            }

        }
        aux += ".*";
        expresion = aux;
        return expresion;
    }

