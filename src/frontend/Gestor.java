    private void jBRegistroLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRegistroLibroActionPerformed
        this.TablaLibJT.setRowSorter(null);
        ArrayList<String> atributos = new ArrayList<>();

        String titulo = this.TituloLibJTF.getText();
        String autor = this.AutorLibJTF.getText();
        String fecha = this.FechaEdLibJTF.getText();
        String cantidad = this.CantidadLibJTF.getText();
        boolean flag = true;
        String mensaje = "";

        if ((titulo.equals("")) || (autor.equals("")) || (fecha.equals("")) || (cantidad.equals(""))) {
            JOptionPane.showMessageDialog(null, "Uno o varios campos están vacíos");
        } else {

            if (!this.verify.formatoCamposAlfabeticos(autor) && flag) {
                mensaje = "Ingrese un nombre válido";
                flag = false;
            }
            if (!this.verify.formatoCamposFecha(fecha) && flag) {
                mensaje = "Ingrese una fecha válida";
                flag = false;
            }
            if (!this.verify.formatoCamposNumericos(cantidad) && flag) {
                mensaje = "Ingrese una cantidad válida";
                flag = false;
            }
            if (flag) {
                try {

                    atributos.add(titulo);
                    atributos.add(autor);
                    atributos.add(fecha);
                    atributos.add(cantidad);
                    reg.registrar("libros", atributos);
                    limpiarTabla(this.TablaLibJT, modeloL);
                } catch (Exception e) {
                }
                this.listarLibros();
            } else {
                JOptionPane.showMessageDialog(null, mensaje);
            }
        }

    }//GEN-LAST:event_jBRegistroLibroActionPerformed

