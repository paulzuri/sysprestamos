    public void listarPrestamos() {
        // remueve las filas de la tabla de prestamos en el JFrame
        limpiarTabla(TablaPrestJT, modeloP);
        
        // construye otro comando SQL para recoger datos de la base de datos
        // (SELECT) recoge todos los datos de la tabla de prestamos
        String sql = "SELECT * FROM bibliotecadb.prestamos";
        try {
            // primero se conecta a la base de datos
            ct = cn.conectarBaseDeDatos();
            // se comienza a armar un comando sql
            st = ct.createStatement();
            // ejecuta el comando en la base de datos
            rs = st.executeQuery(sql);
            
            // para registrar los datos recogidos de sql, es necesario darle un vector a la tabla
            // los datos recogidos se guardan en un vector de objetos para evitar problemas al momento de recoger los datos
            Object[] vectorDeDatosDeTablaDePrestamos = new Object[5];
            
            
            while (rs.next()) {
                vectorDeDatosDeTablaDePrestamos[0] = rs.getInt("id_prestamo");
                vectorDeDatosDeTablaDePrestamos[1] = rs.getInt("codigo_li");
                vectorDeDatosDeTablaDePrestamos[2] = rs.getInt("codigo_es");
                vectorDeDatosDeTablaDePrestamos[3] = rs.getString("fecha_pr");
                
                // guarda los datos en cada una de las filas con el metodo addRow
                
                modeloP.addRow(vectorDeDatosDeTablaDePrestamos);
                
                
            }            
            // establece el modelo de la tabla de prestamos
            this.TablaPrestJT.setModel(modeloP);;
        } catch (Exception e) {

        }
    }
    
