    private void RegistrarEstJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarEstJBActionPerformed
        this.TablaEstJT.setRowSorter(null);
        String tabla = "estudiantes";
        ArrayList<String> atributos = new ArrayList<String>();

        String nombres = this.NombresEstJTF.getText();
        String correo = this.CorreoEstJTF.getText();
        String telefono = this.TelefonoEstJTF.getText();
        boolean flag = true;
        String mensaje = "";

        if ((nombres.equals("")) || (correo.equals("")) || (telefono.equals(""))) {
            JOptionPane.showMessageDialog(null, "Uno o varios campos están vacíos");
        } else {

            if (!this.verify.formatoCamposAlfabeticos(nombres) && flag) {
                mensaje = "Ingrese un nombre válido";
                flag = false;
            }
            if (!this.verify.formatoCamposCorreo(correo) && flag) {
                mensaje = "Ingrese un correo válido";
                flag = false;
            }
            if (!this.verify.formatoCamposNumericos(telefono) && flag) {
                mensaje = "Ingrese un teléfono válido";
                flag = false;
            }
            if (flag) {
                try {
                    atributos.add(nombres);
                    atributos.add(correo);
                    atributos.add(telefono);
                    reg.registrar(tabla, atributos);
                    limpiarTabla(this.TablaEstJT, modeloE);
                } catch (Exception e) {
                }
                this.listarEst();
            } else {
                JOptionPane.showMessageDialog(null, mensaje);
            }
        }

    }//GEN-LAST:event_RegistrarEstJBActionPerformed
    private void filtro() {
        int columna = this.jComboBoxPres.getSelectedIndex();
        //Preparamos la expresion
        String expresion = this.jTFFiltroESTP.getText();
        if (columna == 1) {
            String aux = "";
            expresion = ExpresionRegular(expresion);
        }
        expresion = "^" + expresion;
        tablaFEstudiantes.setRowFilter(RowFilter.regexFilter(expresion, columna));
    }

    private void filtroLibros() {
        int columna = this.jComboBox4.getSelectedIndex();
        //Preparamos la expresion
        String expresion = this.jTFFiltroLibro.getText();
        if (columna == 1 || columna == 2) {
            expresion = ExpresionRegular(expresion);
            expresion = "^" + expresion;
        } else if (columna == 3) {
            String aux2 = "((.{0})| (.{6}))";
            aux2 += "(" + expresion + ").*";
            expresion = aux2;
        }
        TablaFLibros.setRowFilter(RowFilter.regexFilter(expresion, columna));
    }
