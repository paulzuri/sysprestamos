    private void jBActualizarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBActualizarLibroActionPerformed
        this.TablaLibJT.setRowSorter(null);
        ArrayList<String> atributos = new ArrayList<>();
        String cod = this.jTCodLibro1.getText();
        String titulo = this.jTTitulo1.getText();
        String autor = this.jTAutor1.getText();
        String fecha = this.jTFechaL1.getText();
        String cantidad = this.jTCantidadL1.getText();
        boolean flag = true;
        String mensaje = "";

        if (nL > -1) {

            if (this.jCBTitulo.isSelected()) {
                atributos.add("titulo_li = '" + this.jTTitulo1.getText() + "'");
            }
            if (this.jCBAutor.isSelected()) {
                if (!this.verify.formatoCamposAlfabeticos(autor) && flag) {
                    mensaje = "\nIngrese un nombre válido";
                    flag = false;
                } else {
                    atributos.add("autor_li = '" + this.jTAutor1.getText() + "'");
                }
            }
            if (this.jCBFechaL.isSelected()) {
                if (!this.verify.formatoCamposFecha(fecha) && flag) {
                    mensaje = "\nIngrese una fecha válida";
                    flag = false;
                } else {
                    atributos.add("fecha_ed = '" + this.jTFechaL1.getText() + "'");
                }
            }
            if (this.jCBCantidad.isSelected()) {
                if (!this.verify.formatoCamposNumericos(cantidad) && flag) {
                    mensaje = "\nIngrese una cantidad válida";
                    flag = false;
                } else {
                    atributos.add("cantidad = " + this.jTCantidadL1.getText());
                }
            }
            if (flag) {
                try {
                    mod.modificarLibro(atributos, nL);
                    limpiarTabla(this.TablaLibJT, modeloL);
                } catch (Exception e) {
                }
                this.listarLibros();
            } else {
                JOptionPane.showMessageDialog(null, mensaje);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Usuario no seleccionado");
        }
        nL = -1;

    }//GEN-LAST:event_jBActualizarLibroActionPerformed

