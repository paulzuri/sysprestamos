    private int disminuirCantidadLibros(int codLib) {
        // inicializa una variable cantidad en -1
        int cantidad = -1;
        
        // compara i con el numero de filas de la tabla de libros
        // mientras i sea menor que el numero de filas de la tabla de libros
        // va a aumentar i en 1
        
        // getRowCount() entrega el numero de filas de la tabla de Libros
        for (int i = 0; i < this.TablaLibJT.getRowCount(); i++) {
            
            // compara el codigo que se entrego con el codigo del libro en la tabla de libros (primera columna)
            // y luego compara con el resto de libros cuando i incrementa
            if (codLib == Integer.parseInt(this.TablaLibJT.getValueAt(i, 0).toString())) {
                // si el codigo entregado (codLib) es igual al codigo de un libro en la tabla de libros
                // entonces, se crea una variable valorAct con la cantidad de libros en ese momento
                int valorAct = Integer.parseInt(this.TablaLibJT.getValueAt(i, 4).toString());
                
                // si la cantidad de libros (valorAct) es mayor que 0, entonces se quita un libro
                // para hacer el prestamo
                if (valorAct > 0) {
                    cantidad = valorAct - 1;
                    break;
                }
            }
        }
        
        // devuelve la cantidad de libros disminuida en 1
        return cantidad;
    }

    private int aumentarCantidadLibros(int codLib) {
        // inicializa una variable cantidad en -1
        int cantidad = -1;
        
        // compara i con el numero de filas de la tabla de libros
        // mientras i sea menor que el numero de filas de la tabla de libros
        // va a aumentar i en 1 para recorrer la tabla fila por fila
        
        // getRowCount() entrega el numero de filas de la tabla de Libros
        for (int i = 0; i < this.TablaLibJT.getRowCount(); i++) {
            // compara el codigo que se entrego con el codigo del libro en la tabla de libros (primera columna)
            // y luego compara con el resto de libros cuando i incrementa
            if (codLib == Integer.parseInt(this.TablaLibJT.getValueAt(i, 0).toString())) {
                // si el codigo entregado (codLib) es igual al codigo de un libro en la tabla de libros
                // entonces, se crea una variable valorAct con la cantidad de libros en ese momento
                int valorAct = Integer.parseInt(this.TablaLibJT.getValueAt(i, 4).toString());
                // si la cantidad de libros (valorAct) es mayor que 0, entonces se agrega un libro
                // para hacer la entrega
                if (valorAct >= 0) {
                    cantidad = valorAct + 1;
                    break;
                }
            }
        }
        return cantidad;
    }

