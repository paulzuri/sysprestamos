    private void AgregarPrestJBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarPrestJBActionPerformed
        // verify revisa la validacion de datos
        // ctrl + clic lleva a la parte del metodo
        if (verify.formatoCamposNumericos(this.CodEstPresJTF.getText()) && !this.validarNumero(this.CodEstPresJTF.getText())) {
            if (verify.formatoCamposNumericos(this.CodLibPresJTF.getText()) && !this.validarNumeroL(this.CodLibPresJTF.getText())) {
                // crear variables con los datos de los textfield
                int codEst = Integer.parseInt(this.CodEstPresJTF.getText());
                int codLib = Integer.parseInt(this.CodLibPresJTF.getText());
                
                
                int cantidad = disminuirCantidadLibros(codLib);
                
                // si la cantidad es = -1, no se pudo hacer el prestamo y todo este bloque de codigo no se ejecuta
                // si la cantidad es diferente de -1, entonces se pudo hacer un prestamo, y se ejecuta este bloque de codigo
                if (cantidad != -1) {
                    ArrayList<String> atributosParaModificarTablaLibros = new ArrayList<String>();
                    // cantidad que sale de la tabla de libros
                    atributosParaModificarTablaLibros.add("cantidad = " + cantidad);
                    mod.modificarCantidadLib1(atributosParaModificarTablaLibros, codLib);
                    // verifica que la fecha tenga el formato correcto
                    DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");
                    // A un String fecha se le asigna el tiempo actual
                    String fecha = formato.format(LocalDateTime.now());
                    
                    // Se crea un ArrayList con los atributos para hacer el registro en la tabla de prestamos
                    ArrayList<String> atributosParaRegistrarElPrestamoEnLaTablaDePrestamos = new ArrayList<String>();
                    atributosParaRegistrarElPrestamoEnLaTablaDePrestamos.add("" + codLib);
                    atributosParaRegistrarElPrestamoEnLaTablaDePrestamos.add("" + codEst);
                    atributosParaRegistrarElPrestamoEnLaTablaDePrestamos.add(fecha);
                    
                    // se realiza el registro en la base de datos SQL
                    reg.registrar("prestamos", atributosParaRegistrarElPrestamoEnLaTablaDePrestamos);
                    
                    // limpia los campos en el JFrame
                    llenarPrestamoEst("", "", "", "");
                    llenarPrestamoLibro("", "", "", "", "");
                    
                    // ubica al cursor en el JTF de codigo est en la pestaña prestamo
                    this.focusRequester(this.CodigoEstPresJTFDisplay);
                    
                   // ejecuta un comando sql para recoger los datos de prestamo de la base de datos
                   // y luego los registra en la tabla de prestamos de la interfaz con el metodo addRow()
                    listarPrestamos();
                    
                    // borra las filas de la tabla de libros
                    limpiarTabla(TablaLibJT, modeloL);
                    
                    listarLibros();
                } else {
                    // si no se pudo hacer un prestamo, entonces debe ser porque la cantidad de libros es 0
                    JOptionPane.showMessageDialog(null, "Cantidad insuficiente de libros para el préstamo", "No hay suficientes libros", 0);
                    focusRequester(this.CodLibPresJTF);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Campo codigo del libro vacio o erroneo ", "ERROR", JOptionPane.ERROR_MESSAGE);
                focusRequester(this.CodLibPresJTF);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Campo codigo del estudiante vacio o erroneo ", "ERROR", JOptionPane.ERROR_MESSAGE);
            focusRequester(this.CodEstPresJTF);
        }
    }//GEN-LAST:event_AgregarPrestJBActionPerformed

